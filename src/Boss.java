public class Boss {
    private int health;
    private int hit;
    private String protection;


    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }



    public int getHit() {
        return hit;
    }

    public void setHit(int hit) {
        this.hit = hit;
    }



    public String getProtection() {
        return protection;
    }

    public void setProtection(String protection) {
        this.protection = protection;
    }
}
